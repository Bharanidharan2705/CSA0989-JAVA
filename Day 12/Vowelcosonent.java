import java.util.*;
class Vowelconsonent {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
string s= sc.nextLine ();
char ch=s.charAt(0); 
if (ch=='a' || ch=='e' || ch=='i' || ch=='o' || ch=='u')
{
  system.out.print(ch + " is a vowel");
}
 else
{
  system.out.print(ch+"is a consonant");
}
}
}